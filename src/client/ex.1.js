const result = document.getElementsByClassName('result')[0];
const input = document.getElementsByClassName('text')[0];
const sendBtn = document.getElementsByClassName('send')[0];

const ws = new WebSocket('ws://ws.rudenko.tech/nodeschool/ws');

ws.onopen = () => {
	write('Open');
};

ws.onerror = error => {
	write(`Error: ${error}`);
};

ws.onmessage = e => {
	const message = e.data;
	write(`Message: ${message}`);
};

ws.onclose = e => {
	const wasClean = e.wasClean;
	write(`Close wasClean: ${wasClean}`);
};

sendBtn.addEventListener('click', () => {
	const text = input.value;
	ws.send(text);
});

function write(text) {
	result.innerHTML += `${text}<br/>`;
}
